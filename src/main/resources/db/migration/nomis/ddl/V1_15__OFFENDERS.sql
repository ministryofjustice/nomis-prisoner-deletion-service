CREATE INDEX "OFFENDERS_NI3" ON "OFFENDERS" ("LAST_NAME_KEY");
CREATE INDEX "OFFENDERS_NI4" ON "OFFENDERS" ("ALIAS_OFFENDER_ID");
CREATE INDEX "OFFENDERS_NI5" ON "OFFENDERS" ("OFFENDER_ID_DISPLAY");
CREATE INDEX "OFFENDERS_NI6" ON "OFFENDERS" ("ROOT_OFFENDER_ID");
CREATE INDEX "OFFENDERS_NI7" ON "OFFENDERS" ("BIRTH_DATE");
CREATE INDEX "OFFENDERS_NI8_IDS" ON "OFFENDERS" ("OFFENDER_ID", "SEX_CODE");
CREATE INDEX "OFFENDERS_NI9" ON "OFFENDERS" ("LAST_NAME_ALPHA_KEY");
CREATE INDEX "OFFENDERS_X01" ON "OFFENDERS" ("FIRST_NAME", "OFFENDER_ID");
