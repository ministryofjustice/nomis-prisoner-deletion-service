CREATE TABLE GL_TRANSACTIONS (
    TXN_ID NUMBER(10,0) NOT NULL,
    TXN_ENTRY_SEQ NUMBER(6,0) DEFAULT 99,
    GL_ENTRY_SEQ NUMBER(6,0) DEFAULT 99,
    ACCOUNT_PERIOD_ID NUMBER(10,0) NOT NULL,
    ACCOUNT_CODE NUMBER(6,0) NOT NULL,
    TXN_ENTRY_DATE DATE NOT NULL,
    TXN_TYPE VARCHAR2(6) NOT NULL,
    TXN_POST_USAGE VARCHAR2(12) NOT NULL,
    CASELOAD_ID VARCHAR2(6) NOT NULL,
    OFFENDER_ID NUMBER(10,0),
    OFFENDER_BOOK_ID NUMBER(10,0),
    TXN_ENTRY_AMOUNT NUMBER(11,2) NOT NULL,
    TXN_ENTRY_DESC VARCHAR2(240),
    TXN_REFERENCE_NUMBER VARCHAR2(12),
    BANK_STATEMENT_DATE DATE,
    RECON_CLEAR_FLAG VARCHAR2(1) DEFAULT 'N',
    TXN_REVERSED_FLAG VARCHAR2(1) DEFAULT 'N',
    REVERSED_TXN_ID NUMBER(10,0) DEFAULT 99,
    PAYEE_PERSON_ID NUMBER(10,0),
    REVERSED_TXN_ENTRY_SEQ NUMBER(6,0) DEFAULT 99,
    REVERSED_GL_ENTRY_SEQ NUMBER(6,0) DEFAULT 99,
    PAYEE_CORPORATE_ID NUMBER(10,0),
    PAYEE_NAME_TEXT VARCHAR2(60),
    TXN_OBJECT_CODE VARCHAR2(12),
    LIST_SEQ NUMBER(6,0) DEFAULT 99,
    TXN_OBJECT_ID NUMBER(10,0),
    CREATE_USER_ID VARCHAR2(32) DEFAULT USER ,
    CREATE_DATE DATE NOT NULL,
    INFO_NUMBER VARCHAR2(32),
    DEDUCTION_ID NUMBER(10,0),
    TXN_ENTRY_TIME DATE,
    RECEIPT_NUMBER VARCHAR2(13),
    REVERSAL_REASON_CODE VARCHAR2(12),
    TXN_LOC_ID VARCHAR2(6),
    PAYEE_CLEAR_FLAG VARCHAR2(1) DEFAULT 'N',
    CREATE_DATETIME TIMESTAMP DEFAULT systimestamp ,
    MODIFY_DATETIME TIMESTAMP,
    MODIFY_USER_ID VARCHAR2(32),
    AUDIT_TIMESTAMP TIMESTAMP,
    AUDIT_USER_ID VARCHAR2(32),
    AUDIT_MODULE_NAME VARCHAR2(65),
    AUDIT_CLIENT_USER_ID VARCHAR2(64),
    AUDIT_CLIENT_IP_ADDRESS VARCHAR2(39),
    AUDIT_CLIENT_WORKSTATION_NAME VARCHAR2(64),
    AUDIT_ADDITIONAL_INFO VARCHAR2(256),
    CONSTRAINT GL_TRANSACTIONS_PK PRIMARY KEY (TXN_ID,TXN_ENTRY_SEQ,GL_ENTRY_SEQ),
    CONSTRAINT GL_TXN_AC_CODE_F1 FOREIGN KEY (ACCOUNT_CODE) REFERENCES ACCOUNT_CODES(ACCOUNT_CODE),
    CONSTRAINT GL_TXN_CSLD_F3 FOREIGN KEY (CASELOAD_ID) REFERENCES CASELOADS(CASELOAD_ID),
    CONSTRAINT GL_TXN_OFF_NAME_F6 FOREIGN KEY (OFFENDER_ID) REFERENCES OFFENDERS(OFFENDER_ID)
);
CREATE INDEX GL_TRANSACTIONS_NI6 ON GL_TRANSACTIONS (REVERSED_TXN_ID,REVERSED_TXN_ENTRY_SEQ,REVERSED_GL_ENTRY_SEQ);
CREATE INDEX GL_TRANSACTIONS_NI9 ON GL_TRANSACTIONS (CASELOAD_ID,TXN_ENTRY_DATE);
CREATE UNIQUE INDEX GL_TRANSACTIONS_PK ON GL_TRANSACTIONS (TXN_ID,TXN_ENTRY_SEQ,GL_ENTRY_SEQ);
CREATE INDEX GL_TRANSACTION_NI2 ON GL_TRANSACTIONS (OFFENDER_BOOK_ID);
CREATE INDEX GL_TRANSACTION_NI8 ON GL_TRANSACTIONS (ACCOUNT_CODE,ACCOUNT_PERIOD_ID,CASELOAD_ID,TXN_ENTRY_DATE);
CREATE INDEX GL_TXN_AC_PRD_F2 ON GL_TRANSACTIONS (ACCOUNT_PERIOD_ID);
CREATE INDEX GL_TXN_OFF_NAME_F6 ON GL_TRANSACTIONS (OFFENDER_ID);